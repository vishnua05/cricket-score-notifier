package scorenotifierplugin.provider.mobile;

public class MobileMatch {
	private String name;
	private String commentaryURL;
	private String scoreURL;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCommentaryURL() {
		return commentaryURL;
	}
	public void setCommentaryURL(String commentaryURL) {
		this.commentaryURL = commentaryURL;
	}
	public String getScoreURL() {
		return scoreURL;
	}
	public void setScoreURL(String scoreURL) {
		this.scoreURL = scoreURL;
	}
}
